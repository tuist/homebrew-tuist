  desc "Create, maintain, and interact with Xcode projects at scale"
  homepage "https://tuist.io"
  url "https://github.com/tuist/tuist/releases/download/4.10.0/tuist.zip"
  sha256 "d6489b6468782d113bd65b633189a7bcfe9937ae80562a80681632b6e4379ab0"
  license "MIT"
  head "https://github.com/tuist/tuist.git", branch: "main"

